# Operasi Aritmatika
'''
Prioritas operasi:
1. ()
2. **
3. *, /, **, //, %
4. +, -
'''

a = 10
b = 3

# Penjumlahan
hasil = a+b
print(a, "+", b, "=", hasil)

# Pengurangan
hasil = a-b
print(a, "-", b, "=", hasil)

# Pembagian
hasil = a/b
print(a, "/", b, "=", hasil)

# Perkalian
hasil = a*b
print(a, "*", b, "=", hasil)

# Eksponen/Pangkat
hasil = a**b
print(a, "**", b, "=", hasil)

# Sisa hasil pembagian
hasil = a % b
print(a, "%", b, "=", hasil)

# Floor division/pembulatan kebawah
hasil = a // b
print(a, "//", b, "=", hasil)
